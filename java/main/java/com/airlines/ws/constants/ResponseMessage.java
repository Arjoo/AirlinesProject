package com.airlines.ws.constants;


public class ResponseMessage {
	private String msg;

	public ResponseMessage() {
		super();
	}

	public ResponseMessage(String msg) {
		super();
		this.msg = msg;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
}